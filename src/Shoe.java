
public class Shoe {
	String IDShoe;
	String name;
	String category;
	String releaseDate;
	int price;
	
	public Shoe(String IDShoe, String name, String category, String releaseDate, int price) {
		this.IDShoe = IDShoe;
		this.name = name;
		this.category = category;
		this.releaseDate = releaseDate;
		this.price = price;
	}
}
